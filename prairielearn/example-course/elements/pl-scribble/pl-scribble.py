"""pl-scribble — embed the Scribble annotation tool in a PrairieLearn question.

MVP = pure scratchpad (nothing graded or saved server-side).

Why a srcdoc iframe (not <iframe src=...>): PrairieLearn stamps
`Content-Security-Policy: frame-ancestors 'none'` + `X-Frame-Options: DENY` on every
text/html response (apps/prairielearn/src/middlewares/content-security-policy.ts), which
blocks framing a served index.html — even same-origin. A `srcdoc` iframe is generated by the
parent document (no PL HTTP response carries those headers), so the framing block never applies.
A non-sandboxed srcdoc iframe is same-origin with the parent, and a `<base href>` makes every
relative asset URL (app.js, ./*.js modules, pkg/*.wasm, vendor/pdfjs, style.css) resolve to the
deployed bundle under clientFilesCourse/scribble/.

Deploy the COMPILED Scribble bundle to [course]/clientFilesCourse/scribble/ (see prairielearn/deploy.sh).
"""

import html as _html
import os
import re

import lxml.html
import prairielearn as pl


def render(element_html, data):
    if data["panel"] != "question":
        # Scratchpad has nothing to show in the submission/answer panels.
        return ""

    # The annotatable content is THIS element's own children (encapsulation: Scribble
    # never traverses PL's surrounding DOM to find the question). We emit it as a hidden
    # sibling of the iframe inside our own .pl-scribble-wrap; PL/MathJax render it in the
    # live DOM, and embed.js clones it from our wrapper — reading only output we control.
    element = lxml.html.fragment_fromstring(element_html)
    inner = pl.inner_html(element)

    base_url = data["options"]["client_files_course_url"].rstrip("/") + "/scribble/"
    index_path = os.path.join(data["options"]["client_files_course_path"], "scribble", "index.html")

    try:
        with open(index_path, "r", encoding="utf-8") as f:
            doc = f.read()
    except FileNotFoundError:
        return (
            '<div style="padding:12px;border:1px solid #f0d9a8;background:#fff7e6;border-radius:8px;">'
            "<strong>pl-scribble:</strong> the Scribble bundle was not found at "
            "<code>clientFilesCourse/scribble/index.html</code>. Build it and run "
            "<code>prairielearn/deploy.sh</code>.</div>"
        )

    # Inject <base> (resolves all relative URLs to the bundle dir) and an embed flag
    # (srcdoc has no ?embed query string for Scribble to read).
    inject = (
        '<base href="%s">'
        "<script>window.__SCRIBBLE_EMBED = true;</script>"
    ) % _html.escape(base_url, quote=True)
    # Insert right after the opening <head> tag, tolerating attributes/case (a literal
    # "<head>" match silently no-ops on "<head ...>", 404-ing every asset). A lambda
    # replacement sidesteps backslash/backreference escaping in the inject string.
    new_doc, n = re.subn(
        r"<head\b[^>]*>", lambda m: m.group(0) + inject, doc, count=1, flags=re.IGNORECASE
    )
    if n == 0:
        return (
            '<div style="padding:12px;border:1px solid #f0d9a8;background:#fff7e6;border-radius:8px;">'
            "<strong>pl-scribble:</strong> could not inject the asset base — no "
            "&lt;head&gt; found in the Scribble bundle. The bundle may be malformed.</div>"
        )
    doc = new_doc

    srcdoc = _html.escape(doc, quote=True)  # attribute-escape the whole document
    return (
        '<div class="pl-scribble-wrap" style="margin:14px 0;">'
        # The question content, kept in the DOM (so PL/MathJax render it) but hidden —
        # Scribble clones it into the scratchpad. This is our OWN output; embed.js reads
        # frameElement.parentElement and never depends on PrairieLearn's DOM structure.
        '<div class="pl-scribble-source" hidden>%s</div>'
        '<iframe class="pl-scribble-frame" title="Scribble scratchpad" '
        'style="width:100%%;height:660px;border:1px solid #d7dbe0;border-radius:10px;display:block;" '
        'srcdoc="%s"></iframe>'
        "</div>"
    ) % (inner, srcdoc)
